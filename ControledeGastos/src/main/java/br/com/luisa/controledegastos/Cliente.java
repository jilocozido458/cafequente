package br.com.luisa.controledegastos;

/**
 *
 * @author aluno
 */
public class Cliente {

    /**
     * @return the condidentificacao
     */
    public String getCondidentificacao() {
        return condidentificacao;
    }

    /**
     * @param condidentificacao the condidentificacao to set
     */
    public void setCondidentificacao(String condidentificacao) {
        this.condidentificacao = condidentificacao;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the nomeFantasia
     */
    public String getNomeFantasia() {
        return nomeFantasia;
    }

    /**
     * @param nomeFantasia the nomeFantasia to set
     */
    public void setNomeFantasia(String nomeFantasia) {
        this.nomeFantasia = nomeFantasia;
    }

    /**
     * @return the razaoSocial
     */
    public String getRazaoSocial() {
        return razaoSocial;
    }

    /**
     * @param razaoSocial the razaoSocial to set
     */
    public void setRazaoSocial(String razaoSocial) {
        this.razaoSocial = razaoSocial;
    }

    /**
     * @return the tipoCliente
     */
    public String getTipoCliente() {
        return tipoCliente;
    }

    /**
     * @param tipoCliente the tipoCliente to set
     */
    public void setTipoCliente(String tipoCliente) {
        this.tipoCliente = tipoCliente;
    }
     private String condidentificacao; 
    private String email;       
    private String nomeFantasia;
    private String razaoSocial;       
    private String tipoCliente;   

    
}
